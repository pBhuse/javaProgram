
public class FindQuotient {
public static void main(String[] args) {
	System.out.println("========= Q2 Find Quotient And Remainder ========");
	int num1=20;
	System.out.println("Num1="+num1);
	int num2=5;
	System.out.println("Num2="+num2);

	int res1=num1/num2;
	System.out.println("Quotient="+res1);
	int res2=num1%num2;
	System.out.println("Remainder="+res2);

}
}
